package fi.samk;

public class Tehtava3 {

    public double laskeYmpyranPintaAla(double sade) {
        return Math.PI * sade * sade;
    }

    public double laskeNelionPintaAla(double sivu) {
        return sivu * sivu;
    }

    public double laskeSuorakolmionPintaAla(double kanta, double korkeus) {
        return (kanta * korkeus) / 2.0;
    }
}
